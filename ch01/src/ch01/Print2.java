package ch01;

public class Print2 {
	public static void main(String[] args) {
		System.out.println("안에 문자열");
		System.out.println(122);
		System.out.println("87");
		System.out.println("수요일");
		System.out.print("PI는");
		System.out.println(3.14 + "입니다.");
		// \n은 줄바꿈
		System.out.printf("%s : %d원\n", "구구단", 5);
		// 문자로 인식
		System.out.println("3+5 = " + 8 );
		System.out.println(3+5 + " = " + 8);
	}
}
